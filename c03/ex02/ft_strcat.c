char	*ft_strcat(char *dest, char *src)
{
	char *cat;

	cat = dest;
	while (*cat != '\0')
	{
		cat++;
	}
	while (*src != '\0')
	{
		*cat = *(unsigned char *)src;
		cat++;
		src++;
	}
	*cat = '\0';
	return (dest);
}
